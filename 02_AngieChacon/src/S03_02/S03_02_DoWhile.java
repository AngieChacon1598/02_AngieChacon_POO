public class S03_02_DoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        //do = hacer
        //while = mientras
        int a = 100;

        do {
            System.out.println("Hola");

        } while (a == 200);

    }

}
